package com.example.appengine.java8;

public class LambdaPersistenceException extends Exception {

	public LambdaPersistenceException() {
	}

	public LambdaPersistenceException(String message) {
		super(message);
	}

	public LambdaPersistenceException(Throwable cause) {
		super(cause);
	}

	public LambdaPersistenceException(String message, Throwable cause) {
		super(message, cause);
	}

}
